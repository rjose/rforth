	.section .data


#===============================================================================
# BSS section
#===============================================================================
	.section .bss
	.comm test_buffer, 100

#===============================================================================
# TEXT section
#===============================================================================
	.section .text
	.globl main

#-------------------------------------------------------------------------------
# main
#-------------------------------------------------------------------------------
main:
	nop
	movq $test_buffer, %rdi
	call getc

0:	# Exit
	pushq 	$0		# Exit code
	call exit
	addq $8*1, %rsp		# Don't need to remove stack arg, but still :-)
